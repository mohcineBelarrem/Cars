import SwiftUI

extension Color {
    static var lightGray: Color {
        return Color("lightGray")
    }

    static var text: Color {
        return Color("text")
    }

    static var darkGray: Color {
        return Color("darkGray")
    }

    static var specialOrange: Color {
        return Color("orange")
    }

    static var bulletPoint: Color {
        return Color("bulletPoint")
    }

}
